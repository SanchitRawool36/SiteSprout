document.addEventListener('DOMContentLoaded', () => {
  // Enhanced form validation and UX
  const form = document.getElementById('restaurantForm');
  const submitBtn = document.getElementById('submitBtn');
  
  // Auto-generate slug from name
  const nameInput = document.getElementById('name');
  const slugInput = document.getElementById('slug');
  const slugPreview = document.getElementById('slug-preview');
  
  function generateSlug(text) {
    return text
      .toLowerCase()
      .replace(/[^a-z0-9 -]/g, '')
      .replace(/\s+/g, '-')
      .replace(/-+/g, '-')
      .trim('-');
  }
  
  if (nameInput && slugInput) {
    nameInput.addEventListener('input', (e) => {
      if (!slugInput.value || slugInput.dataset.autoGenerated !== 'false') {
        const slug = generateSlug(e.target.value);
        slugInput.value = slug;
        slugInput.dataset.autoGenerated = 'true';
        if (slugPreview) slugPreview.textContent = slug || 'your-restaurant-name';
      }
    });
    
    slugInput.addEventListener('input', (e) => {
      slugInput.dataset.autoGenerated = 'false';
      const slug = generateSlug(e.target.value);
      slugInput.value = slug;
      if (slugPreview) slugPreview.textContent = slug || 'your-restaurant-name';
    });
  }

  // Enhanced color theming with live preview
  const colorInput = document.getElementById('themeColor');
  function updateColor(value) {
    document.documentElement.style.setProperty('--primary-600', value);
    const previewHeader = document.querySelector('.preview-header');
    if (previewHeader) previewHeader.style.backgroundColor = value;
  }
  
  if (colorInput) {
    colorInput.addEventListener('input', (e) => updateColor(e.target.value));
    updateColor(colorInput.value || '#3b82f6');
  }

  // Enhanced live preview updates
  const previewName = document.getElementById('previewName');
  const previewDesc = document.getElementById('previewDescription');
  const previewType = document.getElementById('previewType');
  const previewMenuCount = document.getElementById('previewMenuCount');
  const previewCategoryCount = document.getElementById('previewCategoryCount');
  const businessTypeSelect = document.getElementById('businessType');
  const descInput = document.getElementById('description');
  
  function updatePreview() {
    if (previewName) {
      previewName.textContent = nameInput && nameInput.value.trim() ? nameInput.value.trim() : 'Your Restaurant';
    }
    if (previewDesc) {
      previewDesc.textContent = descInput && descInput.value.trim() ? descInput.value.trim() : 'Short description will appear here.';
    }
    if (previewType && businessTypeSelect) {
      previewType.textContent = 'Type: ' + (businessTypeSelect.value || 'Restaurant');
    }
    
    // Update menu stats
    updateMenuStats();
  }
  
  function updateMenuStats() {
    const categories = document.querySelectorAll('.category-block');
    const totalItems = Array.from(categories).reduce((total, cat) => {
      return total + cat.querySelectorAll('.menu-item-row').length;
    }, 0);
    
    if (previewCategoryCount) previewCategoryCount.textContent = categories.length;
    if (previewMenuCount) previewMenuCount.textContent = totalItems;
  }
  
  if (nameInput) nameInput.addEventListener('input', updatePreview);
  if (descInput) descInput.addEventListener('input', updatePreview);
  if (businessTypeSelect) businessTypeSelect.addEventListener('change', updatePreview);
  
  // Initialize preview
  updatePreview();

  // Enhanced menu builder
  const categoriesContainer = document.getElementById('categoriesContainer');
  const addCategoryBtn = document.getElementById('addCategoryBtn');
  const menuJsonInput = document.getElementById('menuJson');

  function createItemRow(itemName = '', itemPrice = '', itemDesc = '') {
    const row = document.createElement('div');
    row.className = 'menu-item-row';
    
    const nameInput = document.createElement('input');
    nameInput.type = 'text';
    nameInput.placeholder = 'Item name';
    nameInput.className = 'item-name';
    nameInput.value = itemName;
    
    const priceInput = document.createElement('input');
    priceInput.type = 'number';
    priceInput.step = '0.01';
    priceInput.placeholder = 'Price';
    priceInput.className = 'item-price';
    priceInput.value = itemPrice;
    
    const descInput = document.createElement('input');
    descInput.type = 'text';
    descInput.placeholder = 'Description (optional)';
    descInput.className = 'item-desc';
    descInput.value = itemDesc;
    
    const removeBtn = document.createElement('button');
    removeBtn.type = 'button';
    removeBtn.className = 'btn remove-item';
    removeBtn.innerHTML = '×';
    removeBtn.addEventListener('click', () => {
      row.remove();
      updateMenuStats();
    });
    
    // Add event listeners for live updates
    [nameInput, priceInput, descInput].forEach(input => {
      input.addEventListener('input', updateMenuStats);
    });
    
    row.appendChild(nameInput);
    row.appendChild(priceInput);
    row.appendChild(descInput);
    row.appendChild(removeBtn);
    
    return row;
  }

  function createCategoryBlock(categoryName = '') {
    const block = document.createElement('div');
    block.className = 'category-block';
    
    const header = document.createElement('div');
    header.className = 'category-header';
    
    const titleInput = document.createElement('input');
    titleInput.type = 'text';
    titleInput.placeholder = 'Category name (e.g., Starters)';
    titleInput.className = 'category-name';
    titleInput.value = categoryName;
    titleInput.addEventListener('input', updateMenuStats);
    
    const removeCatBtn = document.createElement('button');
    removeCatBtn.type = 'button';
    removeCatBtn.className = 'btn remove-cat';
    removeCatBtn.textContent = 'Remove Category';
    removeCatBtn.addEventListener('click', () => {
      block.remove();
      updateMenuStats();
    });
    
    header.appendChild(titleInput);
    header.appendChild(removeCatBtn);

    const itemsWrap = document.createElement('div');
    itemsWrap.className = 'items-wrap';
    
    const addItemBtn = document.createElement('button');
    addItemBtn.type = 'button';
    addItemBtn.className = 'btn ghost add-item';
    addItemBtn.innerHTML = `
      <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
        <path d="M12 5v14M5 12h14"/>
      </svg>
      Add Item
    `;
    addItemBtn.addEventListener('click', () => {
      const itemRow = createItemRow();
      itemsWrap.appendChild(itemRow);
      updateMenuStats();
    });

    block.appendChild(header);
    block.appendChild(itemsWrap);
    block.appendChild(addItemBtn);
    
    return block;
  }

  if (addCategoryBtn && categoriesContainer) {
    addCategoryBtn.addEventListener('click', () => {
      const categoryBlock = createCategoryBlock();
      categoriesContainer.appendChild(categoryBlock);
      // Add one empty item by default
      const addBtn = categoryBlock.querySelector('.add-item');
      addBtn.click();
      updateMenuStats();
    });
  }

  // Enhanced form submission with loading state
  function buildMenuJson() {
    const categories = Array.from(document.querySelectorAll('.category-block'));
    return categories.map(cat => {
      const categoryName = (cat.querySelector('.category-name') || {}).value || '';
      const items = Array.from(cat.querySelectorAll('.menu-item-row')).map(row => {
        const name = (row.querySelector('.item-name') || {}).value || '';
        const price = (row.querySelector('.item-price') || {}).value || '';
        const description = (row.querySelector('.item-desc') || {}).value || '';
        return { 
          name, 
          price: price === '' ? null : Number(price), 
          description 
        };
      }).filter(item => item.name);
      return { category: categoryName, items };
    }).filter(cat => cat.items.length > 0 || cat.category);
  }

  if (form) {
    form.addEventListener('submit', (e) => {
      // Add loading state
      if (submitBtn) {
        submitBtn.classList.add('loading');
        submitBtn.disabled = true;
      }
      
      // Build and set menu JSON
      const menuData = buildMenuJson();
      if (menuJsonInput) {
        menuJsonInput.value = JSON.stringify(menuData);
      }
      
      // Form validation
      const requiredFields = form.querySelectorAll('[required]');
      let isValid = true;
      
      requiredFields.forEach(field => {
        const group = field.closest('.form-group');
        const feedback = group?.querySelector('.form-feedback');
        
        if (!field.value.trim()) {
          isValid = false;
          group?.classList.add('error');
          if (feedback) feedback.textContent = 'This field is required';
        } else {
          group?.classList.remove('error');
          if (feedback && feedback.classList.contains('error')) {
            feedback.textContent = '';
          }
        }
      });
      
      if (!isValid) {
        e.preventDefault();
        if (submitBtn) {
          submitBtn.classList.remove('loading');
          submitBtn.disabled = false;
        }
        
        // Scroll to first error
        const firstError = form.querySelector('.form-group.error');
        if (firstError) {
          firstError.scrollIntoView({ behavior: 'smooth', block: 'center' });
        }
      }
    });
  }

  // Initialize menu from existing data
  function initializeMenu() {
    if (typeof rawMenuData !== 'undefined' && rawMenuData.length > 0) {
      categoriesContainer.innerHTML = '';
      rawMenuData.forEach(catData => {
        const categoryBlock = createCategoryBlock(catData.category);
        categoriesContainer.appendChild(categoryBlock);
        
        if (catData.items && catData.items.length > 0) {
          const itemsWrap = categoryBlock.querySelector('.items-wrap');
          catData.items.forEach(itemData => {
            const itemRow = createItemRow(itemData.name, itemData.price, itemData.description);
            itemsWrap.appendChild(itemRow);
          });
        }
      });
    } else if (categoriesContainer && categoriesContainer.children.length === 0) {
      // Add default category
      const defaultCategory = createCategoryBlock('Main Dishes');
      categoriesContainer.appendChild(defaultCategory);
      defaultCategory.querySelector('.add-item').click();
    }
    updateMenuStats();
  }

  if (categoriesContainer) {
    initializeMenu();
  }

  // Enhanced image removal with confirmation
  const removedImagesInput = document.getElementById('removedImages');
  const removeImageBtns = document.querySelectorAll('.remove-image');
  let removedImages = [];

  removeImageBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      
      if (confirm('Are you sure you want to remove this image?')) {
        const imageUrl = btn.dataset.imageUrl;
        const container = btn.closest('.gallery-image-container');
        
        if (imageUrl && container) {
          container.style.opacity = '0.5';
          container.style.transform = 'scale(0.9)';
          
          setTimeout(() => {
            container.style.display = 'none';
            removedImages.push(imageUrl);
            if (removedImagesInput) {
              removedImagesInput.value = removedImages.join(',');
            }
          }, 200);
        }
      }
    });
  });

  // File upload preview
  const fileInputs = document.querySelectorAll('input[type="file"]');
  fileInputs.forEach(input => {
    input.addEventListener('change', (e) => {
      const files = e.target.files;
      if (files.length > 0) {
        const group = input.closest('.form-group');
        const feedback = group?.querySelector('.form-feedback');
        
        if (files.length === 1) {
          if (feedback) feedback.textContent = `Selected: ${files[0].name}`;
        } else {
          if (feedback) feedback.textContent = `Selected: ${files.length} files`;
        }
      }
    });
  });

  // Form auto-save (optional enhancement)
  let autoSaveTimeout;
  function autoSave() {
    clearTimeout(autoSaveTimeout);
    autoSaveTimeout = setTimeout(() => {
      const formData = new FormData(form);
      const data = Object.fromEntries(formData.entries());
      localStorage.setItem('restaurant-draft', JSON.stringify(data));
    }, 2000);
  }

  // Add auto-save listeners to form inputs
  const formInputs = form.querySelectorAll('input, textarea, select');
  formInputs.forEach(input => {
    input.addEventListener('input', autoSave);
  });
});

document.addEventListener('DOMContentLoaded', () => {
  const mobileMenuBtn = document.getElementById('mobileMenuBtn');
  const sidebar = document.querySelector('.sidebar');

  if (mobileMenuBtn && sidebar) {
    mobileMenuBtn.addEventListener('click', () => {
      sidebar.classList.toggle('open');
    });
  }
});
